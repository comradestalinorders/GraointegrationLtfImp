package net.is_bg.ltf.grao.ltf;

class GraoCity {
	private String provinceCode;
	private String municipalityCode;
	private String cityEkatte;
	private String cityName;
	private String regionCode;
	private String cityKind;
	private String postCode;
	private String stateCode;
	private String provinceLatinCode;
	private String municipalityEkatte;
	
	public String getProvinceCode() {
		return provinceCode;
	}
	public void setProvinceCode(String provinceCode) {
		this.provinceCode = provinceCode;
	}
	public String getMunicipalityCode() {
		return municipalityCode;
	}
	public void setMunicipalityCode(String municipalityCode) {
		this.municipalityCode = municipalityCode;
	}
	public String getCityEkatte() {
		return cityEkatte;
	}
	public void setCityEkatte(String cityEkatte) {
		this.cityEkatte = cityEkatte;
	}
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public String getRegionCode() {
		return regionCode;
	}
	public void setRegionCode(String regionCode) {
		this.regionCode = regionCode;
	}
	public String getCityKind() {
		return cityKind;
	}
	public void setCityKind(String cityKind) {
		this.cityKind = cityKind;
	}
	public String getPostCode() {
		return postCode;
	}
	public void setPostCode(String postCode) {
		this.postCode = postCode;
	}
	public String getStateCode() {
		return stateCode;
	}
	public void setStateCode(String stateCode) {
		this.stateCode = stateCode;
	}
	public String getProvinceLatinCode() {
		return provinceLatinCode;
	}
	public void setProvinceLatinCode(String provinceLatinCode) {
		this.provinceLatinCode = provinceLatinCode;
	}
	public String getMunicipalityEkatte() {
		return municipalityEkatte;
	}
	public void setMunicipalityEkatte(String municipalityEkatte) {
		this.municipalityEkatte = municipalityEkatte;
	}
	
	
}
